s = int(input('Введите количество секунд: '))
if 0 < s < 61:
    print(f'{s} сек')
if 61 < s < 3601:
    m = s // 60
    m1 = s % 60
    print(f'{m} мин {m1} сек')
if 3601 < s < 86401:
    h = s // 3600
    h1 = (s % 3600) // 60
    h2 = (s % 3600) % 60
    print(f'{h} час {h1} мин {h2} сек')
if 86401 < s < 604801:
    d = s // 86400
    d1 = (s % 86400) // 3600
    d2 = ((s % 86400) % 3600) // 60
    d3 = ((s % 86400) % 3600) % 60
    print(f'{d} дня {d1} час {d2} мин {d3} сек')
